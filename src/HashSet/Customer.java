package HashSet;

public class Customer {
	protected String name;
	protected int cardNo;
	protected int expirayDate;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getCardNo() {
		return cardNo;
	}

	public void setCardNo(int cardNo) {
		this.cardNo = cardNo;
	}

	public int getExpirayDate() {
		return expirayDate;
	}

	public void setExpirayDate(int expirayDate) {
		this.expirayDate = expirayDate;
	}

}
